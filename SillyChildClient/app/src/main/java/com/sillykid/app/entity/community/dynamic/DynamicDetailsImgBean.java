package com.sillykid.app.entity.community.dynamic;

public class DynamicDetailsImgBean {

    private String url;
    private int height;
    private int width;
    private int calculateHeight;
    private int calculateWidth;

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getCalculateHeight() {
        return calculateHeight;
    }

    public void setCalculateHeight(int calculateHeight) {
        this.calculateHeight = calculateHeight;
    }

    public int getCalculateWidth() {
        return calculateWidth;
    }

    public void setCalculateWidth(int calculateWidth) {
        this.calculateWidth = calculateWidth;
    }
}
